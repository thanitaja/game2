<h3>1. การรักษาโดยการใช้ยา</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; สำหรับการรักษาโรคซึมเศร้าด้วยการใช้ยานั้น จะมียาหลากหลายชนิดด้วยกันที่เลือกใช้รักษา ไม่ว่าจะเป็นยาชนิดที่ทำให้ง่วงและไม่ทำให้ง่วง สำหรับยาที่ใช้ในการแก้โรคซึมเศร้าจะไม่ทำให้เกิดการเสพติดแต่อย่างใด นั่นแสดงว่าผู้ป่วยสามารถหยุดรับประทานยาได้เมื่ออาการเริ่มเข้าสู่ภาวะปกติ ในส่วนของยาแก้โรคซึมเศร้านั้นไม่ได้ออกฤทธิ์เพียงเพื่อช่วยลดความกังวลเท่านั้น แต่มันจะทำให้อารมณ์ของคุณหายจากอาการเศร้าได้จริงๆ ทั้งนี้ยาชนิดนี้จะออกฤทธิ์ค่อนข้างช้า ดังนั้นผู้ป่วยจึงต้องทานยาอย่างต่อเนื่องอย่างน้อย 2-3 สัปดาห์ จึงจะสามารถเห็นถึงการเปลี่ยนแปลงหรือมีอารมณ์ที่แจ่มใสขึ้น และสำหรับการใช้ยานั้นมักจะต้องใช้เวลานานถึง 4-6 สัปดาห์ยาจึงจะออกฤทธิ์และแสดงผลลัพธ์ได้อย่างเต็มที่
<br><br>

<h4><u>ยารักษาภาวะซึมเศร้า (Depression Medications)</u></h4> ตามอ้างอิงของ The National Alliance on Mental Illness (NAMI)<br><br>

1. SSRIs (Selective serotonin reuptake)<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เป็นยาลดอาการซึมเศร้า(antidepressants)ที่แพทย์นิยมใช้มากที่สุด ยาเหล่านี้ช่วยบรรเทาภาวะซึมเศร้า(Depression)โดยทำให้มีสารสื่อประสาท serotonin ในสมองมากขึ้น <br><br>


 <div class="alert alert-success">
    <strong>SSRIs ที่ใช้กันมากที่สุด คือ </strong> 	
	<li>Prozac (fluoxetine)</li>
	<li>Zoloft (sertraline)</li>
	<li>Lexapro (escitalopram)</li>
	<li>Paxil (paroxetine)</li>
	<li>Celexa (citalopram)</li>
  </div>


 <div class="alert alert-danger">
    <strong>ผลข้างเคียงที่พบโดยทั่วไปของ SSRIs ได้แก่</strong> 
	<li>ความผิดปกติทางเพศ</li>
	<li>ปัญหาทางเดินอาหารเช่นคลื่นไส้ท้องผูกและท้องร่วง</li>
	<li>ปากแห้ง</li>
	<li>นอนไม่หลับ</li>
	<li>ปวดหัว</li>
	<li>ความวิตกกังวลหรือความกระวนกระวายใจ</li>
	<li>น้ำหนักมากขึ้น</li>
	<li>เหงื่อออก</li>
  </div>

<br><br>

2. Serotonin and Norepinephrine reuptake inhibitors (SNRIs)<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เป็นกลุ่มยาลดอาการซึมเศร้า(antidepressants)ที่นิยมใช้รองลงมา serotonin and norepinephrine reuptake inhibitors (SNRIs) จะยับยั้งการดูดซึมกลับของ serotonin และ norepinephrine ทำให้มีปริมาณสารสองตัวนี้มากขึ้นในสมอง<br><br>

 <div class="alert alert-success">
    <strong>ตัวอย่างยากลุ่มSNRIs เช่น  </strong> 	
	<li>Effexor (venlafaxine)</li>
	<li>Pristiq (desvenlafaxine)</li>
	<li>Cymbalta (duloxetine)</li>
	<li>Fetzima (levomilnacipran)</li>
	<li>Savella (milnacipran) เป็น SNRI แต่ใช้เพื่อรักษา อาการกล้ามเนื้ออ่อนแรง(fibromyalgia) แทนภาวะซึมเศร้า(Depression)</li>
ยาเหล่านี้อาจทำให้เกิดผลข้างเคียงคล้ายคลึงกับยา SSRIs รวมทั้งความเมื่อยล้าและอาการปัสสาวะขัด
  </div>


<br><br>

3. Norepinephrine-dopamine reuptake inhibitor (NDRI)<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ยานี้จะยับยั้งการดูดกลับของสารสื่อประสาท(neurotransmitter)สองตัวคือdopamine และ norepinephrine ตัวอย่างของยากลุ่มนี้ คือ Wellbutrin (bupropion) Wellbutrin มีผลข้างเคียงที่คล้ายคลึงกับยา SSRIs และ SNRIs แต่จะทำให้เกิดความผิดปกติทางเพศน้อยกว่า แต่อาจเพิ่มความเสี่ยงการเกิดอาการชัก<br><br>
<br><br>
4. Tricyclics<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tricyclic antidepressants (tricyclics หรือ TCAs) เป็นยาที่ใช้กันมานานทำงานโดยการยับยั้งการดูดซึมกลับของ serotonin และ norepinephrine โดยกลไกที่แตกต่างจาก SNRIs ยากลุ่มนี้มีการใช้น้อยลงเพราะก่อให้เกิดผลข้างเคียงมากและร้ายแรง จะใช้ยากลุ่มนี้เมื่อใช้ยาตัวอื่นแล้วไม่ได้ผล<br><br>

<div class="alert alert-success">
    <strong>ตัวอย่างของสาร tricyclics ได้แก่   </strong> 	
	<li>Elavil (amitriptyline)</li>
	<li>Norpramin (desipramine)</li>
	<li>Sinequan (doxepin)</li>
	<li>Tofranil (imipramine)</li>
	<li>Pamelor (nortriptyline)</li>
	<li>Avantyl (nortriptyline)</li>
	<li>Vivactil (protriptyline)</li>
  </div>

<div class="alert alert-danger">
    <strong>ผลข้างเคียงที่ร้ายแรงบางอย่างของสาร tricyclics ได้แก่ </strong> 
	<li>มองเห็นภาพซ้อน</li>
	<li>หัวใจเต้นผิดปกติ</li>
	<li>อาการสั่น</li>
	<li>ความคิดสับสนในผู้สูงอายุ</li>
	<li>อาการชัก</li>
  </div>

<br><br>

5. MAOIs<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;monoamine oxidase inhibitors (MAOIs) สามารถยับยั้งเอนไซม์ monoamine oxidase ซึ่งหยุดการทำงานของสารสื่อประสาทต่างๆรวมถึง serotonin และ norepinephrine ในสมอง<br><br>


<div class="alert alert-success">
    <strong>ตัวอย่างของ MAOIs ได้แก่    </strong> 	
	<li>Nardil (phenelzine)</li>
	<li>Marplan (isocarboxazid)</li>
	<li>Parnate (tranylcypromine sulfate)</li>
	<li>Emsam (selegiline) เป็นยาที่พัฒนาล่าสุด ชนิดแผ่นแปะ ซึ่งมีผลข้างเคียงน้อยกว่ายา MAOI อื่น ๆ</li>
	<li>Pamelor (nortriptyline)</li>
	<li>Avantyl (nortriptyline)</li>
	<li>Vivactil (protriptyline)</li>
	เช่นเดียวกับยากลุ่ม tricyclics  ยากลุ่มMAOIs มีการใช้น้อยลงเนื่องจากผลข้างเคียงและปฏิกิริยากับสารอื่นค่อนข้างมาก ตัวอย่างเช่นถ้าคุณกินอาหารที่มีสารประกอบ tyramineจำนวนมาก(พบในชีส ผักดอง และไวน์แดง) พร้อมกับยากลุ่ม MAOI จะเกิดภาวะแทรกซ้อน เช่นอาจเกิดความดันโลหิตสูงรุนแรงที่อาจนำไปสู่โรคหลอดเลือดสมองตีบ(stroke)
  </div>


<div class="alert alert-danger">
    <strong>อาจพบความดันโลหิตสูงเพิ่มขึ้นอย่างรุนแรงหากคุณใช้ยากลุ่ม MAOI ร่วมกับยาเหล่านี้ </strong> 
	<li>ยาคุมกำเนิด</li>
	<li>ยาแก้ปวดบางชนิด</li>
	<li>ยารักษาภูมิแพ้และหวัด</li>
	<li>สมุนไพรบางชนิด</li>
	<li>การใช้ MAOI ร่วมกับ SSRI อาจทำให้เกิดภาวะที่เป็นอันตรายถึงชีวิตได้เช่น serotonin syndrome</li>
	เพิ่มเติม : Serotonin syndrome เกิดจากการงานของระบบ serotonin ในระบบประสาทส่วนกลางทำงานมากผิดปกติ ซึ่งทำให้เกิดอาการผิดปกติทั้ง mental status, ระบบ neuromuscular, ระบบ autonomic ที่ทำงานผิดปกติไป***
  </div>

