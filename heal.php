<h3>2. การรักษาโดยการไม่ใช้ยา</h3>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; สำหรับการรักษาโรคซึมเศร้าโดยไม่ใช้ยานั้นทำได้ด้วยการเปลี่ยนความคิดเพื่อพิชิตความเศร้า และรวมถึงเปลี่ยนพฤติกรรมไปในทางที่ดีขึ้นนั่นก็คือ ผู้ป่วยที่มีอารมณ์เศร้ามักจะมองโลกในแง่ร้าย ซึ่งก็เป็นวัฏจักรที่จะทำให้ภาวะซึมเศร้านั้นอยู่กับตัวผู้ป่วยนาน ดังนั้น เมื่อผู้ป่วยเกิดอารมณ์เศร้าจึงควรบอกและค่อยๆ พูดกับผู้ป่วยให้หยุดเศร้าสักประเดี๋ยว แล้วให้ย้อนกลับไปคิดว่าเมื่อสักครู่เกิดอะไรขึ้น เมื่อสิ่งนั้นเกิดขึ้นแน่นอนว่าจะมีความคิดอะไรบางอย่างแว็บเข้ามาในสมอง จากนั้นให้ลองพิจารณาดูว่าความคิดนั้นถูกต้องแค่ไหน หากคิดว่าไม่ค่อยสมเหตุสมผลเท่าไหร่ อารมณ์จะดีขึ้นทันที อย่างน้อยก็จะทำให้ผู้ป่วยมองโลกในแง่ดีจนกว่าจะเผลอไปคิดในแง่ร้ายอีกครั้ง
<br><br>
1. จิตบำบัดสำหรับโรคซึมเศร้า<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; โรคซึมเศร้านั้นแตกต่างกันไปในแต่ละคน แต่ส่วนใหญ่นั้นจะเกิดขึ้นจากปัจจัยหลายๆปัจจัยมารวมกัน การใช้จิตบำบัดหรือการบำบัดด้วยการพูดคุยถูกออกแบบมาเพื่อช่วยให้ผู้ป่วยรับมือกับปัจจัยด้านจิตใจ พฤติกรรม ปฏิสัมพันธ์กับผู้คน และปัจจัยภาวะแวดล้อมซึ่งเกี่ยวข้องกับโรคซึมเศร้า และการใช้จิตบำบัดนี้ยังช่วยแยกแยะปัจจัยต่างๆเหล่านี้ด้วย โดยโรคซึมเศร้าต่างรูปแบบกันก็มีเป้าหมายการรักษาที่ต่างกันและมีวิธีช่วยเหลือผู้ป่วยต่างกัน เช่น
<br>
