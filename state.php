<h3>1. อาการทั่วไปของโรคซึมเศร้า</h3>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; หากคุณกำลังกังวลว่าตัวเองหรือคนรอบข้างกำลังเผชิญหน้ากับโรคซึมเศร้าอยู่ เบื้องต้นสามารถสังเกตได้จากอาการเศร้า เสียใจ และท้อแท้จนไม่อยากทำอะไร บางครั้งก็เป็นไปอย่างไม่ทราบสาเหตุ ถึงแม้มีเรื่องน่ายินดีก็กลับไม่รู้สึกมีความสุขเลย และนอกจากนี้ ผู้ที่เข้าข่ายเป็นโรคซึมเศร้ามักแสดงอาการเหล่านี้ออกมาพร้อมๆกันภายในระยะเวลา 2 อาทิตย์ 
หากปล่อยไว้นานจะทำให้กลายเป็นโรคซึมเศร้าเรื้อรังซึ่งมีอาการรุนแรงกว่านี้มาก ทางที่ดีควรปรึกษาแพทย์ทันทีเพื่อรับการบำบัดสภาพร่างกายและจิตใจอย่างถูกต้องก่อนที่จะก่อให้เกิดความสูญเสียรู้สึกหมดหวัง ไม่รู้ว่าจะทำอะไรต่อไปหรือจะทำไปเพื่ออะไรไม่มีความสุขกับสิ่งของหรือกิจกรรมที่ชอบ เช่น รู้สึกเบื่อหน่ายอาหารโปรดหรือการร่วมเพศ
น้ำหนักลดลงหรือเพิ่มขึ้นเร็วมากอันเนื่องมาจากการรับประทานอาการที่ไม่เหมือนเดิมนอนไม่หลับมาเป็นระยะเวลานาน แต่ในผู้ป่วยบางรายก็นอนนานผิดปกติมีอาการเครียด หงุดหงิดง่ายขึ้น ไม่รู้สึกผ่อนคลายไม่มีสมาธิ ไม่สามารถจดจ่อกับสิ่งที่ทำได้เลย บางครั้งก็แสดงอาการหลงๆลืมๆ และใช้เวลานานในการตัดสินใจมีการเคลื่อนไหวร่างกายที่ช้าลง รวมถึงการพูด
เพราะรู้สึกว่าร่างกายอ่อนแรงรู้สึกอยากฆ่าตัวตายหรือพยายามฆ่าตัวตาย บางครั้งผู้ที่มีอาการนี้ก็ไม่รู้ตัวเองทั้งนี้ทั้งนั้น บางที่ผู้ที่มีภาวะซึมเศร้าก็อาจจะแสดงออกด้วยวิธีที่แปลกออกไป จนในบางครั้งอาจไม่ได้ทำให้ผู้พบเห็นหรือเจ้าตัวคิดว่าตนเองนั้นเป็น “โรคซึมเศร้า” เรามาลองดูสัญญาณแปลก ๆ เหล่านั้นกันดีกว่า <br> <br>
