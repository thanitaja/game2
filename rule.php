    <h2>ข้อควรปฎิบัติ</h2>
    <p><i> โรคซึมเศร้าส่งผลให้ผู้ป่วยมีอาการป่วยทั้งทางด้านร่างกาย จิตใจและความคิด </i></p>
    <hr><br>    
    &nbsp;&nbsp;&nbsp;&nbsp;<i1><img src="rule1.jpg" width=450px> </i1><i1> &nbsp;&nbsp; <img src="rule2.jpg" width=450px></i1>   <br><br>

<text1> 

<h3>ข้อควรปฏิบัติเมื่อป่วยเป็นโรคซึมเศร้า</h3>
1. ไม่ควรตั้งเป้าหมายในการทำงานมากจนเกินไป และไม่ควรรับผิดชอบทุกอย่างจนเกินความสามารถของตัวเอง<br><br>
2. ควรแยกแยะปัญหาใหญ่ๆ ให้เป็นส่วนย่อย แล้วค่อยๆ เรียบเรียงลำดับความสำคัญก่อนหลังเพื่อลงมือแก้ไข<br><br>
3. ห้ามบังคับตัวเองหรือตั้งเป้าหมายกับตัวเองสูงเกินไป เพราะนั่นจะยิ่งเป็นการเพิ่มความกดดันและเสี่ยงต่อความล้มเหลวในภายหลัง<br><br>
4. หมั่นหากิจกรรมที่ชอบและทำให้ตัวเองรู้สึกดีมาทำบ่อยๆ เพื่อให้เกิดอารมณ์ที่เพลิดเพลินและทำให้มีชีวิตชีวาสดใส<br><br>
5. ไม่ควรตำหนิตัวเอง เมื่อไม่สามารถทำอะไรบางอย่างให้สำเร็จได้ตามที่วางแผนไว้ ควรรู้จักให้อภัยตัวเองและให้โอกาสตัวเองได้เริ่มต้นใหม่<br><br>
<br><hr><br>

<h3>ข้อควรปฏิบัติเมื่ออยู่ร่วมกับป่วยเป็นโรคซึมเศร้า</h3>
1. อย่าพยายามแกล้งทำเป็นไม่เห็น ไม่รับรู้ ไม่สนใจในโรคนี้ การตอกย้ำกับผู้ป่วย หรือคนที่กำลังเครียดว่า นี่มันไม่ใช่เรื่องใหญ่ หรือกระทั่งบอกกับพวกเขาว่า คิดมากไปเองหรือเปล่า ไม่เพียงแต่จะทำให้สถานการณ์เลวร้ายขึ้น หากยังจะยิ่งเป็นเหมือนหอกหนามทิ่มแทงผู้ป่วย ให้ความเศร้ายิ่งดำดิ่งลงไปอีก เพราะฉะนั้นสิ่งแรกที่ควรทำ คือการยอมรับและเข้าใจในความเจ็บปวด และอาการป่วยทางจิตใจของเขาคนนั้น
<br><br>
2. อย่าเอาแต่คิดว่าจะรักษาเขาให้หาย แต่พยายามให้ความรักและความสนใจกับเขาจะดีกว่า สองสิ่งนี้คือสิ่งที่เขาต้องการมากที่สุด
<br><br>
3. หาจุดสมดุลในการมองโลกในแง่ดีให้เจอ จริงอยู่ที่ว่าการพยายามทำตัวสดใส ทำชีวิตให้มีความสุขตลอดเวลา อาจทำให้ผู้ป่วยมีความสุขได้บ้าง แต่ในบางครั้งมันอาจให้ผลลัพธ์ที่ตรงกันข้าม ในกรณีที่ผู้ป่วยไม่สามารถมีความสุขได้ด้วยตนเอง เขาจะยิ่งรู้สึกแย่ เพราะฉะนั้น การมองโลกในแง่ดีเป็นสิ่งที่ดี แต่อย่าไปบังคับมัน หรือฝืนมันจนเกินไป
<br><br>
4. เปิดโอกาสให้พวกเขาได้พูด ได้ระบายออกมา อย่าพยายามหลีกเลี่ยงพวกเขาเพียงเพราะคุณรู้สึกว่าคุณไม่มีอะไรที่จะพูดกับพวกเขา เพราะในหลายๆครั้ง แค่คุณอยู่ตรงนั้น และคอยรับฟังสิ่งที่พวกเขาอยากระบายในเวลาที่เขาพร้อมที่จะพูดออกมา ก็เป็นสิ่งที่ดีที่สุดแล้ว
<br><br>
5. พยายามเข้าใจความรู้สึกของพวกเขา ถึงแม้ว่าในหลายๆครั้ง มันอาจฟังดูไม่มีเหตุผลเลยก็ตาม คุณต้องมั่นใจว่า คุณรับฟังพวกเขาอย่างให้เกียรติ
<br><br>
6. อย่าทิ้งพวกเขาไปไหน ถึงแม้ว่าคุณอาจรู้สึกสับสน และไม่รู้ว่าจะช่วยอะไรได้ คุณควรพยายามทำให้เขารู้สึกว่า ไม่ว่าอย่างไรก็ตาม ถ้าเขาหันหลับมา คุณยังจะอยู่ตรงนั้นเสมอ
<br><br>
7. ชวนพวกเขาไปไหนมาไหนบ้าง โดยมาก ผู้ป่วยโรคซึมเศร้า มักเก็บตัวเงียบ อยู่ห่างไกลผู้คน การพยายามชวนพวกเขาออกจากบ้านบ้าง ก็เป็นเรื่องที่ควรทำ อาจจะเป็นแค่ออกไปทานมื้ออาหารง่ายๆใกล้บ้าน แค่เพียงสถานที่ใหม่ ที่ไม่ใช่ที่เดิมๆที่พวกเขาคุ้นเคย มันก็เพียงพอแล้ว
<br><br>
8. สุดท้าย คุณรักคนอื่นได้ คุณก็ต้องรักตัวเองด้วย การที่เราจะช่วยเหลือบุคคลอื่นได้นั้น ตัวเราเองต้องแข็งแรงก่อน เพราะการดูแลผู้ป่วยโรคซึมเศร้านั้นไม่ง่าย ผู้ดูแลต้องหมั่นดูแลตัวเองให้มีสุขภาพทั้งใจและแข็งแรง ไม่จมดิ่งลงไปตามผู้ป่วย มิเช่นนั้นแล้ว ทั้งผู้ดูแลและผู้ถูกดูแลอาจจะแย่กันทั้งหมด 
<br>
