  <article>
    <h2>ชนิดของโรคซึมเศร้า</h2>
    <p><i>โรคซึมเศร้าแบ่งออกเป็น 3 ชนิดด้วยกัน ซึ่งแต่ละชนิดมีรายละเอียดที่แตกต่างกันไปดังนี้ </i></p>
    <hr><br>    
    <center><i1><img src="type1.jpg" width=450px></i1></center><br><br>

<text1> 

<h3>1. Major Depression (โรคซึมเศร้าแบบรุนแรง)</h3>
&nbsp;&nbsp;โรคซึมเศร้าชนิดนี้เกิดขึ้นเมื่อภาวะซึมเศร้ารบกวนความสุขในชีวิต การทำงาน การเรียน การนอนหลับ นิสัยการกิน และอารมณ์สุนทรีย์  ติดต่อกันอย่างน้อยสองสัปดาห์ บางคนอาจพบแค่เพียงหนึ่งอาการแสดงของภาวะซึมเศร้า ซึ่งอาการของโรคซึมเศร้าชนิดนี้จะเกิดเป็นครั้งๆ แล้วหายไป แต่ทั้งนี้ก็สามารถเกิดได้บ่อยครั้ง <br><br>
<hr><br>
<h3>2. Dysthymia หรือ Persistent Depressive Disorder (โรคซึมเศร้าเรื้อรัง)</h3>
&nbsp;&nbsp;เป็นโรคซึมเศร้าชนิดที่อยู่ในภาวะที่รุนแรง และสามารถเป็นแบบเรื้อรัง จะมีอาการแสดงของอารมณ์ไม่รุนแรงนัก แต่จะเป็นไปอย่างต่อเนื่องอย่างน้อยสองปี ในบางช่วงเวลาของภาวะซึมเศร้าประเภทนี้อาจมีภาวะ major depression ร่วมด้วย ซึ่งจะรบกวนการดำรงชีวิตประจำวัน ทั้งนี้มันสามารถทำให้ผู้ป่วยเกิดการสูญเสียความสามารถในการทำงานและความรู้สึกที่ดีได้<br>
<br><hr><br>
<h3>3. Bipolar หรือ Manic-depressive Illness (โรคซึมเศร้าอารมณ์ตก)</h3>
&nbsp;&nbsp;ผู้มีภาวะซึมเศร้าบางคนอาจมีความผิดปกติแบบอารมณ์สองขั้วร่วมด้วย (bipolar disorder) เป็นโรคซึมเศร้าชนิดที่ผู้ป่วยมีการเปลี่ยนแปลงทางด้านอารมณ์ ซึ่งเป็นลักษณะที่มีอารมณ์แปรปรวนรุนแรงสลับไปมาระหว่างความคิดฟุ้งซ่านขาดสติ (Mania) และภาวะซึมเศร้า (Depression) 
<br><br><hr><br>
 </text1>

<h4><u>ประเภทของภาวะซึมเศร้าอื่นๆ ที่ได้การยอมรับทางการแพทย์ มีดังนี้</u></h4>

<text1>
	
<h3>1. Postpartum Depression (โรคซึมเศร้าหลังคลอดบุตร)</h3>

&nbsp;&nbsp;มารดาหลังภาวะคลอดบุตรมักมีอาการซึมเศร้าที่รุนแรงและใช้เวลากลับปกตินาน ภาวะซึมเศร้าที่คุณแม่มือใหม่มักเจอหลังคลอดจะเรียกว่าแบบ "baby blues"
<br><br><hr><br>
<h3>2. Seasonal Affective Disorder หรือ SAD (โรคซึมเศร้าตามฤดูกาล)</h3>

&nbsp;&nbsp;เป็นภาวะซึมเศร้าในช่วงฤดูหนาว (และบางครั้งก็เกิดภาวะใบไม้ร่วง) มักเกี่ยวข้องกับภูมิอากาศที่มีแสงแดดน้อย
<br><br><hr><br>
<h3>3. Premenstrual Dysphoric Disorder (โรคซึมเศร้าก่อนมีรอบเดือน)</h3>

&nbsp;&nbsp;ซึ่งเป็นอาการซึมเศร้าที่เกิดขึ้นประมาณหนึ่งสัปดาห์ก่อนและหลังช่วงมีประจำเดือนของผู้หญิง
<br><br><hr><br>
<h3>4. Psychotic Depression (โรคซึมเศร้าโรคจิต)</h3>

&nbsp;&nbsp;เป็นภาวะซึมเศร้ารุนแรงที่เกิดในผู้ป่วยโรคจิต(Depression) มักเกิดพร้อมอาการทางจิตเช่น เช่นเห็นภาพลวงตาและภาพหลอน
<br><br><hr><br>

</text1>


