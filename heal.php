	<li>หาความคิดหรือความเชื่อในทางลบหรือผิดจากความเป็นจริงที่ส่งผลต่ออารมณ์ความรู้สึกที่ซึมเศร้า</li>
	<li>สร้างความสามารถที่จะรับมือกับความเครียดและแก้ปัญหาได้ดีขึ้น (mirtazapine)</li>
