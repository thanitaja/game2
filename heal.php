</ul>
 </div>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; จิตบำบัดสองรูปแบบที่ใช้มากที่สุดคือพฤติกรรมและปัญญาบำบัด(cognitive behavioral therapy)หรือซีบีที(CBT) และปฏิสัมพันธ์บำบัด(interpersonal therapy) โดยพฤติกรรมปัญญาบำบัดนั้นพยายามที่จะช่วยให้ผู้ที่ซึมเศร้าค้นพบความคิดและความเชื่อในทางลบหรือไม่ดีต่อสุขภาพและเปลี่ยนความคิดและความเชื่อเหล่านั้นเป็นในทางบวกซึ่งมีประสิทธิภาพในการบำบัดความเจ็บป่วยทางจิตใจได้หลายประเภท ผู้ที่เข้าร่วมจิตบำบัดมักจะมีการบ้านให้กลับไปทำโดยให้บันทึกความคิดด้านลบที่เกิดขึ้นกับสิ่งต่างๆ

ส่วนปฏิสัมพันธ์บำบัดจะเน้นไปที่การค้นหาความสัมพันธ์ของบุคคล แยกแยะปัญหาของความสัมพันธ์นั้น และพัฒนาทักษะด้านมนุษยสัมพันธ์ โดยมีเป้าหมายเพื่อช่วยให้ค้นพบรูปแบบทางสังคมในเชิงลบของตนเอง เช่น การแยกตัวจากสังคมและความก้าวร้าว และช่วยพัฒนาวิธีในการโต้ตอบกับคนอื่นได้ดีขึ้น

สถาบันสุขภาพจิตแห่งชาติ(National Institute of Mental Health)ระบุว่าจิตบำบัดเพียงอย่างเดียวอาจเป็นทางเลือกที่ดีที่สุดสำหรับผู้ที่เป็นโรคซึมเศร้ารุนแรงเล็กน้อยถึงปานกลาง แต่อาจไม่เพียงพอสำหรับผู้ที่ซึมเศร้ารุนแรงมาก
<br><br><br>


2. การบำบัดด้วยการช็อตไฟฟ้าสำหรับโรคซึมเศร้า<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ถ้าจิตบำบัดและยาใช้ไม่ได้ผล จิตแพทย์อาจแนะนำให้ใช้การบำบัดด้วยการกระตุ้นสมอง การบำบัดด้วยการช็อตไฟฟ้า(electroconvulsive therapy)หรืออีซีที(ECT)มีใช้มานานมากแล้วโดยใช้ครั้งแรกในปี 1940 ซึ่งการบำบัดด้วยการช็อตไฟฟ้านั้นจะใช้กระแสไฟฟ้าปล่อยผ่านสมองขณะที่ดมยาสลบอยู่ การรักษานี้จะทำให้ชักช่วงสั้นๆซึ่งควบคุมได้โดยส่งผลต่อเซลล์ประสาทและสารเคมีในสมอง ตามข้อมูลจากเครือข่ายความเจ็บป่วยทางจิตใจแห่งชาติ(National Alliance on Mental Illness)คนส่วนใหญ่จะต้องรักษาสี่ถึงหกครั้งก่อนจะเริ่มเห็นการเปลี่ยนแปลงที่ดีขึ้นอย่างเห็นได้ชัด การช็อตไฟฟ้านี้จะมีผลข้างเคียงชั่วคราวได้แก่ ปวดหัว ปวดกล้ามเนื้อ คลื่นไส้ สับสน และความจำเสื่อม

<br><br><br>

3. การกระตุ้มสมองด้วยแม่เหล็กสำหรับโรคซึมเศร้า<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;แทนที่จะใช้กระแสไฟฟ้า การกระตุ้นสมองด้วยแม่เหล็ก(transcranial magnetic stimulation)หรือทีเอ็มเอส(TMS)จะใช้สนามแม่เหล็กกระตุ้นเซลล์ประสาทและช่วยบรรเทาอาการซึมเศร้า การรักษานี้ไม่ต้องใช้ยาดมสลบและเน้นไปที่บริเวณของสมองที่เกี่ยวกับการควบคุมอารมณ์ ผลข้างเคียงจากการกระตุ้นสมองด้วยแม่เหล็กนั้นได้แก่ กล้ามเนื้อใบหน้ากระตุก ปวดหัวหรือเวียนหัว และชัก(ถ้าเคยชักมาก่อน)

<br><br><br>

4. การกระตุ้นเส้นประสาทวากัส (vagus nerve) สำหรับโรคซึมเศร้า<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;สำหรับโรคซึมเศร้าเรื้อรังหรือโรคซึมเศร้าที่ไม่ตอบสนองต่อการบำบัดด้วยการช็อตไฟฟ้าหรือการกระตุ้นสมองด้วยแม่เหล็กนั้น การกระตุ้นเส้นประสาทวากัส(vagus nerve stimulation)หรือวีเอ็นเอส(VNS)ก็เป็นทางเลือกหนึ่ง การรักษาวิธีนี้จะใช้อุปกรณ์ฝังเข้าไปเพื่อกระตุ้นเส้นประสาทวากัสด้วยสัญญาณไฟฟ้าซึ่งทำหน้าที่ส่งกระแสประสาทไปส่วนของสมองที่ควบคุมอารมณ์และการนอนหลับตลอดทั้งวันซึ่งถือเป็นเครื่องมือที่สร้างจังหวะให้กับสมอง ผลข้างเคียงเฉพาะที่ของการรักษาด้วยการกระตุ้นเส้นประสาทวากัสเช่นปัญหาเกี่ยวกับช่องปาก(กลารกลืน ความปวด และการไอ) ปวดคอ และปัญหาการหายใจระหว่างออกกำลังกาย

<br><br><br>

5. ธรรมชาติบำบัดสำหรับโรคซึมเศร้า<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;มีการใช้ธรรมชาติบำบัดหลายวิธีเช่นเดียวกับการรักษาทางเลือกหรือการรักษาเสริมซึ่งอาจช่วยรักษาโรคซึมเศร้าเมื่อใช้ร่วมกับการรักษาวิธีอื่น(รวมถึงการใช้ยา) การรักษาเหล่านี้ได้แก่

<br>
<div class="alert alert-success">
<ul class="b">
	<li>ออกกำลังกายซึ่งช่วยหลั่งฮอร์โมนกระตุ้นอารมณ์</li>
	<li>โยคะ การทำสมาธิ และการฝึกจิตอื่นๆช่วยลดความเครียดและบรรเทาอารมณ์เชิงลบได้</li>
	<li>การยวดช่วยลดฮอร์โมนความเครียดและเพิ่มสารสื่อประสาทที่ช่วยให้อารมณ์คงที่</li>
	<li>การฝังเข็มอาจส่งผลดีต่อสารสื่อประสาท</li>
	<li>อาหารเสริมบางชนิด เช่น โฟเลต (folate)เอสเอ็มอี (SAMe) หรือเอส-อะดีโนซิล-เอล-เมทไธโอนีน (S-Adenosyl-L-Methionine) และหญ้าเซนต์จอห์นเวิร์ธ (St. John's wort) อาจช่วยรักษาโรคซึมเศร้า แต่ยังต้องมีการวิจัยเพิ่มเติมเพื่อพิสูจน์ผลในการรักษา</li>
	
</ul>
 </div>

