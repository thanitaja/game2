<h3>2. อาการของโรคซึมเศร้าตามเพศและอายุ</h3><br>

<h4><U>อาการของโรคซึมเศร้าในผู้ใหญ่</U><h4><br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; อาการของโรคซึมเศร้าอาจแตกต่างกันไปในแต่ละบุคคล แต่ส่วนมากผู้ป่วยมักมีอาการดังต่อไปนี้<br><br>
<ul class="b">
	<li>รู้สึกเศร้าหรือว่างเปล่าเป็นเวลานาน </li><br>
	<li>รู้สึกหงุดหงิด กระสับกระส่าย หรือกระวนกระวายใจอยู่บ่อย ๆ </li><br>
	<li>รู้สึกสิ้นหวัง ไม่มีค่า หรือรู้สึกผิด </li><br>
	<li>อ่อนเพลีย </li><br>
	<li>พฤติกรรมการกินที่เปลี่ยนไป </li><br>
	<li>มีความลำบากในการมีสมาธิ จดจำรายละเอียด หรือทำการตัดสินใจ </li><br>
	<li>ปัญหาการนอนหลับ เช่น นอนหลับมากขึ้น หรือ นอนไม่หลับ </li><br>
	<li>รู้สึกหมดความสนใจในกิจกรรมหรืองานอดิเรกเดิมที่เคยสนใจ </li><br>
	<li>มีอาการปวดศีรษะ ปวดเมื่อยตามตัวที่ไม่สามารถอธิบายสาเหตุได้ หรือมีปัญหาเกี่ยวกับการย่อยอาหาร </li><br>
	<li>มีความคิดเรื่องความตายและการฆ่าตัวตาย </li><br>
	<li>คิด พูด และทำงานช้าลง </li><br>
	<li>สะเพร่า </li><br>
	<li>เสพสารเสพติด </li><br>

</ul>


<h4><U>อาการของโรคซึมเศร้าในผู้ชาย</U><h4><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; แม้ว่าผู้ชายและผู้หญิงต่างมีอาการของโรคซึมเศร้าได้เหมือนกัน แต่ก็มีอาการเฉพาะเจาะจงบางอย่างที่มีความแตกต่างอย่างมีนัยสำคัญ อ้างอิงจาก 2013 report in the journal JAMA Psychiatry ผู้ชายที่มีโรคซึมเศร้ามักจะพบอาการดังต่อไปนี้ได้มากกว่าผู้หญิง<br><br>

<ul class="b">
	<li>โกรธ </li><br>
	<li>ก้าวร้าว</li><br>
	<li>การเสพยาหรือการใช้แอลกอฮอล์ </li><br>
	<li>พฤติกรรมเสี่ยงต่าง ๆ (risk-taking behaviour) </li><br>
	

</ul>


<h4><U>อาการของโรคซึมเศร้าในผู้หญิง</U><h4><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ผู้หญิงจะเกิดโรคซึมเศร้าได้มากกว่าผู้ชายถึง 70% อ้างอิงจาก The National Institutes of Health. จากแหล่งข้อมูลอื่น ๆ รวมถึง 2013 JAMA Psychiatry report กล่าวไว้ว่าผู้หญิงมีโอกาสที่จะเป็นโรคซึมเศร้าได้มากถึงสองเท่า ผู้หญิงที่มีโรคซึมเศร้ามักจะพบว่ามีอาการดังต่อไปนี้<br><br>

<ul class="b">
	<li>เครียด</li><br>
	<li>แยกตัวสันโดษ</li><br>
	<li>กระสับกระส่าย </li><br>
	<li>มีปัญหาด้านการนอนหลับ</li><br>
	<li>สูญเสียความสนใจเดิม</li><br>
	
</ul>

<h4><U>อาการของโรคซึมเศร้าในวัยรุ่น</U><h4><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; วัยรุ่นก็สามารถมีอาการของโรคซึมเศร้าได้เหมือนผู้ใหญ่ แต่ว่าอารมณ์และพฤติกรรมที่เปลี่ยนแปลงนั้นมักถูกเข้าใจผิดว่าเป็นภาวะปกติของการเข้าสู่ช่วงการเป็นวัยรุ่น อาการแสดงอื่น ๆ ที่บอกถึงโรคซึมเศร้าในวัยรุ่นได้แก่<br><br>


<ul class="b">
	<li>หมกมุ่นเรื่องความตาย เช่น กลอนหรือภาพวาดที่บ่งบอกถึงความตาย</li><br>
	<li>มีพฤติกรรมอาชญกรรม เช่น การขโมยของในร้าน</li><br>
	<li>มีการแยกตัวออกจากครอบครัวและเพื่อน </li><br>
	<li>อ่อนไหวต่อการวิพากย์วิจารณ์</li><br>
	<li>ผลการเรียนแย่ลงหรือขาดเรียน</li><br>
	<li>พฤติกรรมเสี่ยง เช่น การมีเพศสัมพันธ์ที่ไม่ปลอดภัยและการขับรถโดยประมาท</li><br>
	<li>การดื่มแอลกอฮอล์หรือการใช้สารเสพติด</li><br>
	<li>มีพฤติกรรมที่ไม่สมเหตุสมผลหรือแปลกประหลาดไปจากเดิม</li><br>
	<li>มีการเปลี่ยนแปลงของลักษณะนิสัยหรือการแต่งตัวภายนอกอย่างสิ้นเชิง</li><br>
	<li>ทิ้งข้าวของของตัวเอง</li><br>
	
</ul>

<br><hr><br>

<h3>3. อาการตามชนิดของโรคซึมเศร้า</h3><br>


<h4><U>อาการของโรคซึมเศร้าชนิด Major Depression</U><h4><br>

<ul class="b">
	<li>มีการเปลี่ยนแปลงทางอารมณ์ ผู้ป่วยจะมีความรู้สึกซึมเศร้า มีความกังวลบ่อยๆ มีอารมณ์ที่หงุดหงิดฉุนเฉียว โกรธง่าย อยู่ไม่สุขและมักรู้สึกกระวนกระวายอยู่ตลอดเวลา</li><br>
	<li>มีการเปลี่ยนแปลงทางความคิด ผู้ป่วยจะรู้สึกสิ้นหวังต่อการใช้ชีวิต มักมองโลกในแง่ร้าย รู้สึกผิดหวังบ่อยๆ มักคิดว่าตัวเองไม่มีค่า มีความคิดที่จะทำร้ายตัวเอง คิดถึงแต่ความตาย และพยายามทำร้ายตัวเองอยู่บ่อยครั้ง</li><br>
	<li>มีการเปลี่ยนแปลงทางการเรียนรู้และการทำงาน ผู้ป่วยจะไม่สนใจต่อสิ่งแวดล้อม ไม่สนใจเรื่องที่จะทำให้เกิดความสนุกสนานกับชีวิต ไม่สนใจงานอดิเรก เพิกเฉยต่อกิจกรรมที่น่าสนใจต่างๆ มีความรู้สึกอ่อนเพลีย ไม่มีพลังงาน ทำงานได้ช้าและมีคุณภาพต่ำลง ไม่มีสมาธิต่อสิ่งที่ทำ การตัดสินใจแย่ลงและความจำเสื่อม </li><br>
	<li>มีการเปลี่ยนแปลงทางพฤติกรรม ผู้ป่วยมีอาการนอนไม่หลับ มีพฤติกรรมตื่นนอนเร็ว ในผู้ป่วยบางรายจะหลับนานเกินไป เบื่ออาหารจึงส่งผลทำให้น้ำหนักลด ในขณะที่บางคนรับประทานอาหารมากเกินไปจึงทำให้น้ำหนักเพิ่มขึ้น เมื่อมีอาการป่วยมักรักษาด้วยยาธรรมดาไม่หาย เช่น ปวดศีรษะ ปวดท้อง แน่นท้อง และปวดเรื้อรัง ที่สำคัญผู้ป่วยยังมีความสัมพันธ์กับบุคคลรอบตัวอย่างแย่ลงด้วย</li><br>	
</ul>

<h4><U>อาการของโรคซึมเศร้าชนิด Dysthymia</U><h4><br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ผู้ป่วยจะมีอาการซึมเศร้าต่อเนื่องเรื้อรังเป็นเวลาติดต่อกันยาวนาน อาจถึงขั้นสูญเสียหน้าที่การงาน การใช้ชีวิต รู้สึกไม่พึงพอใจในตนเอง ไม่มีความสุขตลอดเวลา มองโลกในแง่ร้าย แต่ผู้ป่วยยังคงใช้ชีวิตประจำวันได้ปกติ แต่มักไม่ค่อยมีความพอใจเมื่อมีสิ่งดีๆเข้ามาในชีวิต เสมือนหนึ่งเป็นคนขาดชีวิตชีวา ขาดวิญญาณซึ่งโรคซึมเศร้าแบบ Dysthymia นี้มีสิทธิ์เปลี่ยนเป็นโรคซึมเศร้าแบบรุนแรงได้ (Major depression)
<br><br>

<h4><U>อาการของโรคซึมเศร้าชนิด Bipolar หรือ Manic-depressive illness</U><h4><br>


&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; สำหรับผู้ป่วยที่มีอาการป่วยเป็นโรคซึมเศร้าชนิดนี้จะมีอาการร่าเริงเกินกว่าเหตุ หงุดหงิดง่าย นอนน้อยลงจากเดิม มักหลงผิดคิดว่าตัวเองเก่ง ตัวเองใหญ่ มีพฤติกรรมพูดมาก ชอบคิดที่จะแข่งขัน มีความต้องการทางเพศสูง มีพลังงานมาก ตัดสินใจในแต่ละครั้งไม่ดี และมีพฤติกรรมหลายอย่างเปลี่ยนแปลงไปจากเดิมอย่างชัดเจน

<br><br>
<h4><U>ความแตกต่างของโรคซึมเศร้า และโรคเครียด</U><h4><br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; โรคซึมเศร้ากับโรคเครียด ดูเหมือนจะมีลักษณะอาการที่ใกล้เคียงกัน จนทำให้บางครั้งผู้ป่วยไม่สามารถแยกตัวเองออกมาได้ว่า อาการที่เกิดขึ้นมาจากความเครียด หรือโรคซึมเศร้ากันแน่ เราสามารถมีวิธีสังเกตความแตกต่างระหว่างทั้ง 2 โรคได้ด้วยอาการขั้นพื้นฐานดังนี้<br><br>


<h4>อาการที่มาจากความเครียด (Stress)</h4>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ความเครียดหรือโรคเครียด โดยทั่วไปจะเป็นเพียงอาการที่เกิดขึ้นในระยะสั้นๆ เท่านั้น หากเป็นความเครียดธรรมดา สามารถเกิดขึ้นได้กับทุกคน ทุกเพศทุกวัย ความเครียดจะไปสั่งผลกระตุ้นทั้งในแง่ดีและแง่ลบ ขึ้นอยู่กับว่าเราสามารถตั้งรับกับความเครียดได้มากน้อยแค่ไหน โดยพบว่าอาการทั่วไปของความเครียดคือ
<br><br>
<ul class="b">
	<li>ไม่สามารถทนต่อความรู้สึกได้</li>
	<li>นอนหลับยาก หรือนอนไม่หลับ</li>
	<li>กระสับกระส่าย กังวล และเหมือนจะประสาทเสีย </li>
	<li>รู้สึกไม่มีแรง หมดแรง</li>
	<li>รู้สึกว่าปัญหาที่เข้ามาในชีวิตยุ่งยากมากเกินจนรับได้</li>
	<li>มีปัญหาเรื่องสมาธิและความจำสั้น</li>
	<li>การดื่มแอลกอฮอล์หรือการใช้สารเสพติด</li>
	<li>มีพฤติกรรมที่ไม่สมเหตุสมผลหรือแปลกประหลาดไปจากเดิม</li>
	<li>มีการเปลี่ยนแปลงของลักษณะนิสัยหรือการแต่งตัวภายนอกอย่างสิ้นเชิง</li>
	<li>ทิ้งข้าวของของตัวเอง</li><br>
	
</ul>

<hr>


<div class="alert alert-info">
    <strong><h4><text2><u>แตกต่างจากความเครียดอย่างไร? </u></text2></h4></strong> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ความแตกต่างของโรคนี้กับความเครียดคือ จะเกิดขึ้นใน "ระยะยาว" การปล่อยผ่านโดยรู้สึกว่ามันเป็นแค่ความเครียด จะไม่ช่วยทำให้อาการดีขึ้น แต่จะยิ่งแย่ลงเรื่อยๆ ผู้ป่วยจึงจำเป็นต้องได้รับการรักษา เนื่องจากสารเคมีในสมองผิดปกติ เพราะความเศร้าจะควบคุมความรู้สึกอยู่ตลอดเวลา
