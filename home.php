    <h2>โรคซึมเศร้า (major depressive disorder : MDD)</h2>
    <p><i>โรคซึมเศร้าเป็นโรคทางจิตเวช สามารถเกิดขึ้นได้กับทุกเพศทุกวัย โรคนี้มักมาจากมรสุมชีวิตและการสูญเสีย มีอาการคล้ายที่เห็นใกล้เคียงกับอาการเศร้าหรือเสียใจทั่วไป</i></p>
    <hr><br>    
    <center><img src="home1.jpg" width=450px></center><br><br>

<text1> <hl>โรคซึมเศร้า </hl> สามารถเกิดขึ้นได้กับทุกเพศทุกวัย โรคซึมเศร้าในผู้สูงอายุหรือในเด็กเองก็เกิดขึ้นได้ เมื่อดูจากภายนอกแล้วอาการที่เห็นใกล้เคียงกับอาการเศร้าหรือเสียใจทั่วไป แต่ผลกระทบนั้นรุนแรงกว่ามาก ซึ่งทั้งตัวผู้ป่วยเองหรือคนรอบข้างอาจไม่ทันสังเกตเห็นด้วยซ้ำ 

ปัญหาส่วนใหญ่คนที่ป่วยเป็นซึมเศร้ามักจะไม่รู้ตัวเองว่าตนกำลังป่วยเป็นซึมเศร้า หรืออาจรู้ตัวอีกทีตอนที่โรคกำลังพัฒนาไปสู่ขั้นรุนแรงจนเกิดผลกระทบต่อการใช้ชีวิตประจำวันไปแล้ว ทำให้โรคนี้กลายเป็นอีกหนึ่งโรคร้ายที่พรากหลายต่อหลายชีวิตไปอย่างคาดไม่ถึง

ผู้ป่วยโรคซึมเศร้านั้นไม่ได้ถูกจัดประเภทว่าเป็นคนบ้าหรือคนไม่ดีแต่อย่างใด หากแต่เป็นผู้ป่วยที่มีอาการป่วยทางอารมณ์ที่ควรได้รับการรักษา เพราะหากปล่อยไว้โดยไม่ได้รับการรักษาในเร็ววันอาจส่งผลเสียถึงชีวิตอย่างหนักได้ โดยอาจจะมีพฤติกรรมคิดสั้นฆ่าตัวตาย 

ดังนั้น สาเหตุของการเกิดโรค อาการและวิธีรักษานับว่าจำเป็นยิ่งนักที่ทุกคนจะต้องให้ความใส่ใจตระหนักรู้ โดยคุณสามารถติดตามรายละเอียดดังกล่าวได้ดังต่อไปนี้ โรคซึมเศร้าสามารถรักษาให้หายขาดได้ด้วยยาและการทำจิตบำบัดและผู้ป่วยจะต้องเข้ารับการรักษาตั้งแต่เริ่มมีอาการ 

 ระยะเวลาในการรักษาอาจใช้เวลานานตั้งแต่ 3 เดือน หรือ 6 เดือน เป็นต้นไป ทั้งนี้ขึ้นอยู่กับความรุนแรงของโรคและการให้ความร่วมมือในการรักษาจากผู้ป่วยและญาติ </text1><br><br><hr>

<h3>สาเหตุของโรคซึมเศร้า</h3>

<text1>
	โรคซึมเศร้าเกิดจากความผิดปกติของสารเคมีที่อยู่ในสมองที่มีชื่อว่า เซโรโทนิน (serotonin) และนอร์เอพิเนฟริน (norepinephrine) เมื่อสารเคมีดังกล่าวมีปริมาณน้อยลงจากเดิมก็ทำให้ผู้ป่วยมีอาการป่วยทั้งทางด้านร่างกาย จิตใจและทางความคิด ซึ่งโดยรวมจะสังเกตเห็นได้ว่าผู้ป่วยจะมีความรู้สึกท้อแท้ เบื่อหน่าย เหงา ไม่มีชีวิตชีวา ไม่สนุกสนานกับชีวิตประจำวัน ระสับกระส่าย อยากอยู่คนเดีย นอนไม่หลับ มักสะดุ้งตื่นในกลางดึก ฝันร้ายบ่อย เหล่านี้ยังเป็นพฤติกรรมที่ส่งผลกระทบต่อความสามารถในการทำงานที่ลดลง

สำหรับสาเหตุที่ทำให้เกิดอาการซึมเศร้านั้นมาจากหลากหลายปัจจัยด้วยกัน ไม่ว่าจะเป็นในด้านของกรรมพันธุ์ ด้านพัฒนาการของจิตใจ รวมทั้งด้านสิ่งแวดล้อมที่ต้องเผชิญ ตัวอย่างเช่น ผู้ป่วยประสบกับความเครียดที่แสนหนัก เจอมรสุมชีวิตที่ไม่ทันได้ตั้งตัว เกิดอาการเจ็บป่วยเรื้อรังจนทำให้หมดกำลังใจ ตกงาน มีปัญหาเรื่องการเงินที่หาทางออกไม่ได้ มีปัญหาเกี่ยวกับความสัมพันธ์กับคนใกล้ชิด รวมทั้งพบเจอกับความสูญเสียในชีวิตที่ทำให้เสียใจเป็นอย่างมาก ไม่ว่าจะเป็นการสูญเสียพ่อแม่ในขณะที่ยังอยู่ในช่วงของวัยเด็ก สูญเสียคนรัก สูญเสียครอบครัว และยังรวมถึงปัจจัยทางชีวภาพ เช่น เกิดการเปลี่ยนแปลงของระดับสารเคมีในสมองบางชนิด ก็สามารถส่งผลทำให้เกิดโรคซึมเศร้าได้เช่นเดียวกัน รวมถึงการใช้สารเสพติดที่อาจทำให้สารเคมีในสมองผิดปกติ

โดยสรุปแล้วปัจจัยที่อาจก่อให้เกิดอาการซึมเศร้าได้เช่น
<br><br>

<ul class="b">
  <li>ภาวะเจ็บป่วยที่สามารถทำให้เกิดการเสียชีวิต (เช่น โรคมะเร็งหรืออาการปวดเรื้อรัง)</li>
  <li>ภาวะโดดเดี่ยวทางสังคม</li>
  <li>ชีวิตที่เครียด (เช่นปัญหาการหย่าร้างหรือขัดสนเงินทอง)</li>
  <li>พันธุกรรม (มีความผิดปกติของอารมณ์และการฆ่าตัวตายของคนในครอบครัว)</li>
  <li>ภาวะบาดเจ็บหรือการถูกล่วงละเมิดในวัยเด็กซึ่งทำให้เกิดการเปลี่ยนแปลงระยะยาวจากการสั่งการของสมองเพื่อจัดการกับความกลัวและความเครียด)</li>
  <li>โครงสร้างสมองและการใช้สารเสพติด</li>
  <li>ชีวิตที่เครียด (เช่นปัญหาการหย่าร้างหรือขัดสนเงินทอง)</li>
  <li>การเปลี่ยนแปลงของฮอร์โมนเช่นฮอร์โมนจากภาวะตั้งครรภ์หรือภาวะต่อมไทรอยด์ผิดปกติ</li>
  <li>ผู้หญิงมีแนวโน้มที่จะมีภาวะซึมเศร้า(Depression) มากกว่าผู้ชายถึง 70 เปอร์เซ็นต์</li>
  <li>คนที่มีอายุระหว่าง 18-25 ปีมีแนวโน้มที่จะมีภาวะซึมเศร้า มากกว่าคนที่มีอายุตั้งแต่ 50 ปีขึ้นไปถึงร้อยละ 60</li>
</ul>
